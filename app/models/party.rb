class Party
  def initialize (name, date, hour, link, image, id)
    @name = name
    @date = date
    @hour = hour
    @link = link
    @image = image
    @id = id
  end

  def name
    @name
  end

  def date
    @date
  end

  def hour
    @hour
  end

  def link
    @link
  end

  def image
    @image
  end

  def id
    @id
  end
end
